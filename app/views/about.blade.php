    <h2>About Go Fast Express</h2>
    <p>Go Fast Express Inc. is a transport company based in Mississauga, Ontario that prides itself on customer satisfaction. We are dedicated to building good relationships with our customers and bringing the best price to the table. We do trips across Canada and some of the United States.</p>
    <p>We are confident in our ability to drive down costs in your transportation spending. Our goal is to reduce your overall expense while maximizing supply chain management efficiency. As well as direct savings, you will see indirect savings in several other areas such as spending money and time to fix a mix-up or mistake. We are able to do this because one of our main philosophies at Go Fast Express is to provide our employees with a sense of ownership. We do this by involving our staff in discussions and decision making. By talking to our employees, we are able to see from their point of view, enabling us to improve our services where needed immediately. This philosophy has remained useful over the years, as proven by our consistently punctual deliveries, claim free/damage free shipments, and our excellent customer service.</p>

    <h3>History</h3>
    <p>Go Fast Express was started by our CEO, Mr. Tarachand Isree in 2004. With a single straight truck, he provided transportation services in the greater Toronto area. With the goal of expanding the company in mind, he has grown Go Fast Express to a company with over 10 tractors, 13 trailers and 15 experienced employees. Go Fast Express currently provides complete LTL and TL coverage in various provinces throughout Canada.</p>
